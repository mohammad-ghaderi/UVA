struct Node {
    int val;
    Node *left = nullptr, *right = nullptr;
};

Node *insert(int value, Node *root) {
    Node *nd = new Node;
    nd->val = value;
    if (root == nullptr) return nd;

    Node *nxt = root, *tree = root;
    
    while(nxt != nullptr) {
        tree = nxt;
        if (value > nxt->val) nxt = nxt->right;
        else nxt = nxt->left;

    }

    if (value > tree->val) tree->right = nd;
    else tree->left = nd;

    return root;
}

void post_order(Node *root) {
    
    if (root == nullptr) return;

    post_order(root->left);
    post_order(root->right);
    cout << root->val << endl;
}

